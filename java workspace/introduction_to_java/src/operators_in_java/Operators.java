package operators_in_java;

public class Operators {
	public static void main(String[] args) {
		int a = 10;
		a++; //post increment
		++a; //pre increment
		a--;
		--a;
		//=,+=,-=,/=,*=,%=
		
		
		/*
		Arithmetic Operators - +,-,*,/,%
		Unary Operators - ++,--
		Assignment Operator - = ,+=,-=,*=,/=,%=
		Relational Operators - <,>,<=,>=,!=,== - true or false
		Logical Operators - && , ||, ! - true or false
		Ternary Operator - ?:
		Bitwise Operators - &, |, ~
		Shift Operators -  >>,<<
		instance of operator - instanceOf
		*/
	}
}
