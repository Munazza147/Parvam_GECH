package introduction_to_java.first_program;

public class PrimetiveDatatypes {
	public static void main(String[] args) {
		//PrimetiveDatatypes
		/*
		 * 1. byte = 23 (default = 0)
		 * 2. short = 300 (default = 0)
		 * 3. int = 3000 (default = 0)
		 * 4. long = 300L (default = 0l)
		 * 5. float = 8.9f (default = 0.0f)
		 * 6. double= 9.1 (default = 0.0)
		 * 7. char = 'a' (default = '')
		 * 8. boolean = true (default = false)
		 * */
		
		//declerartion variable
		/*
		 * variable - it is container used ot store a values based on 
		 * the data type
		 * <datatype> variable_name ;
		 * */
		byte age;
		
		// variable name = value;
		age = 20;
		
		// <datatype> variable name = value;
		
		byte age1 = 30;
		
		System.out.println(age);
		System.out.println(age1);

		
	}
}	
